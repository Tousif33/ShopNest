export const updateUser = async (req, res) => {
  try {
    const userIdToUpdate = req.params.userId;
    const loggedInUser = req.user;

    const {
      firstName,
      lastName,
      email,
      address,
      city,
      zipCode,
      phoneno,
      role
    } = req.body;

    

    // Authorization
    if (
      loggedInUser._id.toString() !== userIdToUpdate &&
      loggedInUser.role !== "admin"
    ) {
      return res.status(403).json({
        success: false,
        message: "You are not allowed to update profile",
      });
    }

    const user = await User.findById(userIdToUpdate);

    if (!user) {
      return res.status(404).json({
        success: false,
        message: "User not found",
      });
    }
      
    if (role === "admin" || role === "user") {
  user.role = role;
}
    // Upload new profile picture
    if (req.file) {
      const oldPublicId = user.profilePicPublicId;

      if (oldPublicId) {
        try {
          await cloudinary.uploader.destroy(oldPublicId);
        } catch (err) {
          console.log("Cloudinary delete failed:", err.message);
        }
      }

      const uploadResult = await new Promise((resolve, reject) => {
        const stream = cloudinary.uploader.upload_stream(
          {
            folder: "profile",
          },
          (error, result) => {
            if (error) reject(error);
            else resolve(result);
          }
        );

        stream.end(req.file.buffer);
      });

      user.profilePic = uploadResult.secure_url;
      user.profilePicPublicId = uploadResult.public_id;
    }

    // Basic fields
    user.firstName = firstName ?? user.firstName;
    user.lastName = lastName ?? user.lastName;
    user.email = email ?? user.email;

    // Address object
    if (!user.address) {
      user.address = {};
    }

    user.address.street = address ?? user.address.street;
    user.address.city = city ?? user.address.city;
    user.address.pincode = zipCode ?? user.address.pincode;
    user.address.phoneno = phoneno ?? user.address.phoneno;

    const updatedUser = await user.save();

    return res.status(200).json({
      success: true,
      message: "Profile updated successfully",
      user: updatedUser,
    });
  } catch (error) {
    console.log(error);

    return res.status(500).json({
      success: false,
      message: error.message,
    });
  }
};